package soal3;

public class operasi {
    int i,j,k,l,m,n,o,p;

    void tambah(){
        int tambah = i + j ;
        System.out.println("penambahan: " + tambah);
    }

    void kurang(){
        int kurang = k - l;
        System.out.println("pengurangan:  " +kurang);
    }

    void kali(){
        int kali = m * n;
        System.out.println("perkalian:  " + kali);
    }

    void bagi(){
        int bagi = o / p;
        System.out.println("pembagian:  " + bagi);
    }
}
